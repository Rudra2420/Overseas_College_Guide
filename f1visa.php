      <h3>F1 Visa Processing Time</h3>
      <p>The processing time for F1 Visa is quite less in comparison to other visas. In the majority of the cases, candidates come to know about the faith of their visa immediately after the interview. Whether you will get the visa or not, you will come to know about it once the interview is over. If the visa is rejected then the visa officer will you hand over the documents mentioning the reason for denial. In certain cases, processing and knowing the result of the visa can take time, in that case, you have to be patient and wait for the result.</p><br>

      <h3>Working on F1 Visa</h3>
      <p>Students cannot work off-campus during the first year of their program. They are allowed to work for 20 hours per week. However, during holidays and breaks, they can work up to 40 hours per week. Students are allowed to work for a commercial firm that provides services to the university/college, like cafeteria or bookstore. Those who want to work outside the campus have two options:</p>
      <ul>
      	<li>Curriculum Practical Training (CPT)</li>
      	<li>Optional Practical Training (OPT)</li>
      </ul>


      <h3>Curriculum Practical Training (CPT)</h3>
      <p>CPT allows students to work or intern as part of their academic curriculum. The internship/practical training is mandatory for completing their degree. Moreover, the internship must be related to their area of study and also be approved by their university or educational institution.</p>

      <p>Candidates are allowed to start their Curriculum Practical Training (CPT) after completing 9 months of enrollment in their program. CPT is semester-specific, it is only authorized on a semester basis i.e. fall, spring or summer CPT. Its duration is from one semester up to 364 days.</p><br>

      <h3>Optional Practical Training (OPT)</h3>
      <p>The OPT is designed to give international students the opportunity to work in the US. With OPT, students are allowed to work maximum for a period of 12 months. Also, one can only work in their field of study and must get a job within 90 days after graduation. For those who are studying STEM subjects can get OPT extension for another 24 months. One can only start working under OPT if the candidate and his/her University's dedicated official has filed for the Employment Authorization Document (EAD). You have to fill form I-765. Also, if you are lucky, your employer will sponsor your H1-B Visa after which one can also file for a Green Card.</p><br>

      <h3>Dependents</h3>
      <p>Spouses and children under the age of 21 who wish to accompany the aspirant in the US for the duration of his/her stay require F2 or M2 visas. A spouse on F2 status cannot enroll for a full course of study but can take classes that are vocational or recreational in nature like pursuing a hobby or interest, such as cooking or tennis. Children on F2 status are allowed to attend elementary, middle, or high school as a full-time student. They may not enroll in a full-time course of study at a college or university.</p>

      <p>The spouse and children of students cannot accept employment at any time during the stay-period on the F2 Visa.</p>


      <h3>F1 Visa Health Insurance Requirements</h3>
      <p>There are no specific requirements set by the US Department of State for F1 Visa. Generally, the school/ educational institution themselves decide what kind of insurance students need to take. In case, the school has not asked for any specific requirements for the health insurance than the applicant themselves decide what kind of insurance they required to take.</p>

      <h3>What you can do with F1 Visa?</h3>
      <p><b>Change of visa status:</b>If in case you find a job which requires a different type of visa, then F1 visa holders can change the status of their visa. In this scenario, you need to follow the instructions of US Citizenship and Immigration Services (USCIS) or your employer or your sponsor/reviewer. </p>

      <p><b>Extend period of stay:</b>Generally, the validity of F1 Visa is for the time period of mentioned in the I-20 form or according to the duration of the course. However, if your visa does not allow you to stay for the complete duration of your course, then you have to apply for an extension of your student visa. The visa extension is given to F1 Visa holders on the pretext that they will be completing their course in the US and will also return to their home country once the course is over. </p> 

      <p><b>Access to US healthcare: </b>As an F1 Visa holder, you can have access to healthcare facilities in the US. Although the cost of medical facilities is one of the costliest in the world, so, the students have been advised to take an appropriate health insurance plan as suggested by your university/educational institution.</p>

      <h3>F1 Visa Rejection</h3>
      <p>If in case your visa application is rejected, then the reason for rejection of visa and under which section it has been denied is provided to you by the visa officer. Whether the visa is rejected because the applicant is not able to provide supporting documentation or if the candidate was not eligible or any other reason, the candidate comes to know about it immediately after the completion of the visa interview.</p>

      <h3>Reapply for a Visa</h3>
      <p>Those who found ineligible for study visa can reapply for it. If in case the visa is rejected under 221(g), in that case, the candidate needs to submit the visa application along with the visa fee again. Those who are denied visa under section 214(b) of the INA, then they can present evidence of significant changes done in the application since your last application.</p>

