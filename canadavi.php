			Canada continues to attract a steady volume of students from India. While getting admission to Top Universities in Canada continues to require excellent academic score, the permission to study is validated by means of a Study Permit. If you wish to take academic, professional, or vocational training at a university, college, or any other educational institution in Canada, you will need a study permit before you enter Canada. This study permit is issued to students when they arrive in Canada. However, to get there, a student must first apply for a Temporary Resident Visa which is provided by the Canadian High Commission and facilitated by the Visa Application Centers. VFS Global Services is the designated VACs for Canadian Visa. 
		</p>
		<H2>Eligibility criteria for Canada Student Visa</H2>
		<p>You can apply for a Canada Study visa if you fulfill the following criteria:</p>
		<ul>
		  <li><p >You have been accepted by a designated learning institute (DLI).</p></li>
		  <li><p>You need to prove that you have adequate funds to pay your tuition fee and living expenses.</p></li>
		  <li><p>You need to prove that you have a clean background and no criminal record. The aspirants need to produce a police certificate to prove this.</p></li>
		  <li><p>You need to do a health checkup and produce a medical certificate that certifies that you are in good health.</p></li>
		</ul>
		<p>Also at the time of the visa interview, you need to convince the visa officer that you will leave Canada once you complete your studies.</p>
		<h2>Which Documents are required for Canadian Visa?</h2>
		<p>You should apply for the student visa after you get the college acceptance letter. You should begin with the visa process around June if aiming at the September intake. The following are the documents needed to apply For Canada Student Visa.</p>
		<h3>1. Valid Passport:</h3>
		<p>You would need a Valid Passport to be able to apply for a Study Permit. As of the Canadian High Commission, it is important that you have a passport whose validity covers the intended stay in Canada. For instance, if you plan to travel to Canada in September 2019 for a two-year course, your passport should be valid until at least September 2021.</p>
		<h3>2. Proof of Acceptance by a Designated Learning Institution:</h3>
		<p>You would need the acceptance letter from the university/institute you are planning to attend. A Designated Learning Institute is the University which is recognized by the Immigration Department. (Here’s a list for your reference). In case you are applying for Quebec, you would also need a CAQ which you would be duly notified.</p>
		<h3>3. Proof of Funds</h3>
		<p>At the time of application for your Study Permit, you would have to show proof of funds. As per the present standards, you would have to prove that you would have enough funds to pay our tuition fees as well as take care of living expenses. The Canadian Immigration deems a student would require at least CAD 10,000 for every year of your stay. Apart from the above two, the student would also have to prove that he/she has enough funds for a return fair as well.</p><br>
		<p><b>Note:</b></p>
		<p><b>While filling the application form, you would have to show the proof of fees paid as well as the stipulated cost of living @ CAD 10,000. If you are applying for Quebec you require at least CAD 11,000 for every year of your stay.</b></p><br>

		<h3>4. Passport Size Photographs</h3>
		<p>If you have opted for offline application, you would need two passport-sized photographs that conform to the given standards. For online application, you must procure a digital copy of the photograph which should not be more than 4MB. Other requirements are standard about neutral facial expression, plain background as well as no headgear (unless for religious purposes). Note:
     
      <ul>
       <li>Size of the image should be at least 35 mm x 45 mm</li>
      <li> Image should be recent (not older than 6 months)</li>
     </ul>
     <h3>5. Immigration Medical Examination (IME)</h3>
     <p>Canadian Immigration requires international students from India to undergo a compulsory Immigration Medical Examination from empanelled doctors. Students would have to book an appointment and visit the listed doctors for a medical examination, preferably a week before they start their Visa Application. This is to give the doctor enough time to validate and upload the necessary documents. Booking an appointment in advance with the nearest facility/ practitioner as per the list of empanelled doctors is always advised.</p>
     <p>The panel physician will perform a complete medical exam and may refer you for chest x-rays and laboratory tests. Once your exam has been completed, the physician will send the results to CIC.</p>
     <h3>6. English Language Proficiency Exam Score</h3>
     <p>Though not mandatory to have at the time of application, we strongly recommend that you have appeared for and get your English Language Proficiency Score before you start your Visa Application Process. As it is, you would have had to submit your English language proficiency score to confirm your admission to the Canadian University. TOEFL, IELTS, etc. are all acceptable.</p>
     <h3>7. Statement of Purpose</h3>
     <p>When applying for a Canadian Study Permit, you would be required to submit an essay stating the purpose of your trip to Canada and why you have chosen the particular institution. This would be prompted as an optional document in the checklist but we strongly recommend that you submit the same.</p><br>

     <h2>How to Apply for a Canadian Visa for Study</h2>
     <h4>There are several steps to apply for a visa.</h4>

     <ol>
     	<li><b> Check the processing times -</b>The visa application cycle might take anywhere from 3-4 weeks. It is advisable to go through the Canadian consulate website to know the approximate time taken to process the student visa. Please note that the time shown is not a guarantee and is just an estimate.</li>
     	<li><b> Determine how you will apply. -</b>There are two ways to apply:</li>
     	(a)<a href="https://www.canada.ca/en/immigration-refugees-citizenship/services/come-canada-tool.html">Apply online</a>
     	<p>To apply online you must have access to a scanner or camera to create electronic copies of your documents for uploading and have a valid credit card for payment. You will still be required to provide a finger scan at the regional Visa Application Centre (VAC). Remember, in the case of Online Application you need to visit the VFS offices to hand your passports and would have to provide the confirmation of form and fee payment as well. the steps would only vary in terms of documents you need to carry to the VAC.</p>
     	(b)<b>Apply in person</b>
     	<p>Download the application kit and read the instruction guide. The instruction guide contains important information about study permits and instructions to help you complete your application. Read the guide carefully and use the document checklist.</p>
     	<li> Fill out the <a href="https://visa.vfsglobal.com/ind/en/can/"> VFS Consent Form </a>and attach it with your application form.  You need this form to get the assistance of VFS services for your visa filing process.</li>
     	<li><b> Pay the processing fee -</b>Note that when submitting an application via the VFS, in person or by mail, visa fees must be paid in addition to VFS Global service charges. The processing fee is non-refundable in all cases.</li>
     	<li>If your spouse/common-law partner and/or children are accompanying you and you are applying for temporary resident visas, work permits, or study permits for them, then you will need to pay the appropriate processing fees for them. </li>
     	<li><b>Submit your application and supporting documents - </b>You have to visit the nearest VFS office. Once you are there, pay the service charges and hand them your complete application to obtain a receipt. This receipt contains your unique tracking number which you will need to track the progress of your application online.</li>
     </ol>
     <p>In case your application is approved you will receive notification from the Government of Canada requesting your passport. You can submit the passport and passport request letter in person or through VFS. You would be provided with a Temporary Resident Visa and a Letter of Introduction.</p>
    </ol>
    
	<h3><b>Processing Time for the Canada Student Visa</h3></b>
    <p>Processing time is the time taken by the CIC for processing a visa application. It starts when the aspirant files the application until the final decision day. As such, there is no specific time period mentioned for processing a Canada study visa application. Aspirants (both online and paper) can view the status of their applications through their MyCIC account.</p>
    <h3><b>Study Permit Renewal</b></h3>
    <p>For staying in Canada, students need to have a valid study permit. If in case, your study permit expires before completion of your studies (program) then you need to apply for renewal of study permit. Ideally, you should apply at least30 days before your current permit expires. Candidates can submit their application through mail or online.</p>
    <h3><b>Dependents</b></h3>
    <p>Spouses can accompany full-time students on a Dependent visa. You only need to show that adequate funds are available for their support. Spouses can also work full time if his/her stay is for a year or more.</p>
    <p>In some cases, you may apply for an open work permit if you will be accompanying your spouse or common-law partner to Canada. With an open work permit, you may find and accept any job when you get to Canada. You do not need a job offer or a positive labor market impact assessment to apply for your work permit.</p><br>

