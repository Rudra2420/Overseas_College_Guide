		  <h3>Documents required for a UK Study Visa</h3>
		   <p>UK Student Visa Requirements: Applying for a student visa requires you to carry a lot more documents in comparison to a tourist visa, by comparison. Aspirants need the mandatory documents along with supporting documents of financial and academic records. Make sure</p>
		   <p>You should have the following documents before applying:</p>
		   <ul>
		     <li>A completed student visa application form</li>
		   	 <li>A letter of acceptance on the course - Your approved education provider will give you an acceptance letter (CAS) and a visa letter which are official and unconditional offers of a place on a course of study. The visa letter must be issued no more than six months before you apply.  The applications where the visa letters are older than six months will be refused.</li>
		   	 <li>Having a visa letter does not guarantee the application will be successful. The applicant must meet all the requirements of the category and any other requirements of the immigration rules.</li>
		   	 <li>Proof of funds - The money you need to show covers your course fees for your first year of study and living costs for up to a maximum of nine months. The amount you will need depends on whether you are applying as a child or adult and also whether you will be studying in or out of London. You must show that you have held the money for at least 28 days. The end of that 28 day period must not be more than one month before the date of your application.</li>
		   	</ul>

		   	<h3>UK Student Visa Process: How to Apply</h3>
		   	<p>There are several steps to apply for a UK Student Visa. UK Study Visa applications are processed within three weeks of the completed application being lodged.</p>
		   	<p><b>Step 1 - </b>The first step is to check if you qualify for the student visa or not. You need to show that you have:</p>
		   	<ul>
		   		<li>A confirmed place at the school, university or college where you intend to study. It is called a ‘confirmation of acceptance for studies’ (CAS).</li>
		   		<li>Enough money (also known as ‘maintenance or funds’) to cover your course fees and living costs in the UK.</li>
		   	</ul>

		   	<p><b>Step 2 -</b> Get your documents ready</p>
		   	<p><b>Step 3 -</b> Apply online<br>
		   		You will need to register and create an account on the official UK Visa website.
                When you have completed your online application form, you will need to print and sign your completed form. You must use the online appointment calendar to book an appointment at the visa application center. When you have completed your online application, you will receive an email message containing your application number.</p>
            <p><b>Step 4 -</b> Pay your application fee. There are 4 ways to pay your visa application fee.
            </p>    
            <ul>
            	<li>Payment at Standard Chartered Bank - You can pay your fee at some branches of the Standard Chartered Bank. The bank will issue a demand draft in favor of the 'The British High Commission'.</li>
            	<li>Demand draft from a nationalized or foreign bank - You can pay your fee by getting a demand draft issued by a nationalized or a foreign bank. Bank charges for this service will vary. The demand draft must be in favor of the 'The British High Commission’.</li>
                <li>Payment at the visa application center - You can pay the visa application fee at the visa application center when you submit your visa application.</li>
                <li>Pay online - You can pay your visa application fee online using a Visa or MasterCard credit or debit card.</li>
            </ul>

            <p><b>Step 5 – </b>Visit the visa application center on the appointed date. You must book and attend an appointment at the visa application center to submit your application and facial photograph and fingerprints.</p>

